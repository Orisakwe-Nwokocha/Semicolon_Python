num1 = int(input("Enter first number:\n"))
num2 = int(input("Enter second number:\n"))
num3 = int(input("Enter third number:\n"))

print("The sum is", num1 + num2 + num3)
print("The average is", (num1 + num2 + num3) / 3)
print("The product is", num1 * num2 * num3)
print("The smallest number is", min(num1, num2, num3))
print("The largest number is", max(num1, num2, num3))